package javaStart.Ch2variable;

public class Var7 {
    public static void main(String[] args) {
        int a=100;
        System.out.println("a = " + a);
        double b =10.5;
        System.out.println("b = " + b);
        boolean c = true;
        System.out.println("c = " + c);
        char d= 'A';
        System.out.println("d = " + d);
        String e = "hello java";
        System.out.println("e = " + e);

    }
}
